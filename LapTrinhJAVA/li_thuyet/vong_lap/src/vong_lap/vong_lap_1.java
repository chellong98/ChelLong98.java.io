public class vong_lap_1 {

    public static void main(String[] args) {
        int i;
        for (i = 0; i < 10; i++) {
            System.out.println("i: " + i);
        }
        System.out.println();
        int j = 0;
        while (j < 10) {
            System.out.println("j: " + j);
            j++;
        }
        System.out.println();
        int k = 0;
        do {
            System.out.println("k: " + k);
            k++;
        } while (k < 10);
    }
}
